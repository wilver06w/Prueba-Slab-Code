class Informacion {
  String name;
  int facturacion;
  int costosOperativos;
  int nivelEjecucion;
  int numeroOperarios;
  int cantidadEnergiaGenerada;

  Informacion({
    required this.name,
    required this.facturacion,
    required this.costosOperativos,
    required this.nivelEjecucion,
    required this.numeroOperarios,
    required this.cantidadEnergiaGenerada,
  });
}
